function initialize() {
    const olElement = document.querySelector('ol');
    const rangeInput = document.querySelector('input[type="range"]');
    const counterElement = document.getElementById('counter');
    const showHideButton = document.querySelector('button');
    updateList(olElement, 5);

    rangeInput.addEventListener('input', () => {
        const value = rangeInput.value;
        counterElement.innerText = value;
        updateList(olElement, value);
    });

    //q6 
    showHideButton.addEventListener('click', () => {
        if (olElement.style.display === 'none') {
            olElement.style.display = 'block';
        } else {
            olElement.style.display = 'none';
        }
    });


    olElement.addEventListener('click', event => {
        if (event.target.tagName === 'LI') {
            event.target.style.color = 'blue';
            console.log(event.target.innerText);
        }
    });
}

function updateList(olElement, count) {
    olElement.innerHTML = '';
    for (let i = 0; i < count; i++) {
        const listItem = document.createElement('li');
        listItem.innerText = data[i].name;
        olElement.appendChild(listItem);
    }
}




